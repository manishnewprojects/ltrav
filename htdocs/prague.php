 Prague is one of the most beautiful and very tourist friendly cities I have been to. The whole city is very walkable and unlike some other European cities, will not bite into your wallet. The Czech republic is a part of the European Union, but they did not adopt the Euro as their currency. The local currency is the Czech Koruna and when I visited in April 2018, $1 was worth about 20 Koruna. You can start exploring thec ity by going to the Charles Bridge, a very centrally located historic bridge on the Vlatava (Vol-ta-va) river. The bridge itself is a quite fascinating and historic. I would advise you to get there early in the morning if possible before all the tourists arrive, so you can enjoy the beauty of a structure itself along with the views of the river and the buildings along the river. We stayed at an AirBnb that was within walking distance, so getting there early was easy.  On the bridge itself, you will find many interesting statues and plaques. Each of those have a somewhat of an interesting history. I was not really impressed with the food choices by the bridge. The Charles Bridge area seemed kind of touristy and expensive. If a restaurant has a big, bold sign saying "authentic food", that is actually a big warning sign for me.  Pick up a Bagel or two from one of the local bakeries and that should be a good start for the day.

One of my favorites is of the knight with a dog. I think societies that treat their animals well generally reflect a good human nature. 

There's also a plaque with an interesting tale of a monk who was thrown over the bridge into the river and eventually came back as a martyr. 

A short walk will lead you to the fascinating old Jewish section of town. Here you will find a lot of interesting sites, including a Jewish cemetery or a historic synagogue. In a testament to the resilience of people, we can see that inspite of the Nazi atrocities, the Jewish population in Prague not only to survived but is now thriving. 

There is a clock on the top of a building with Hebrew letters. I thought that was kind of interesting. 

As you're walking away from the bridge, look up to the hill on your left. You will see an interesting structure which looks like a gigantic metronome. At one time this location had a gigantic monument to Stalin and the Soviet Union by the Czech people. Eventually it was blown up and replaced with the current display. The Stalin monument has a very interesting history, which you can see in detail at the Museum of Communism. Dont miss this musuem.




